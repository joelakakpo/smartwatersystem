pH = np.clip(pH, 6.0, 9.0)

# Turbidity: values between 0 and 3000 NTU
turbidity = np.random.uniform(low=0, high=3000, size=n_samples)

# Total Dissolved Solids (TDS): around 300 mg/L
total_dissolved_solids = np.random.normal(loc=300, scale=50, size=n_samples)
total_dissolved_solids = np.clip(total_dissolved_solids, 50, 1000) # Reasonable range

# Temperature: around 20 Celsius
temperature = np.random.normal(loc=20, scale=5, size=n_samples)
temperature = np.clip(temperature, 0, 35) # Reasonable range

# 4. Create a binary target variable 'water_safe'
